package com.bjss.desk_booking.DTO;

public class UserBooking {

    private int userId;
    private int bookingId;

    public UserBooking(){
        this.userId = userId;
        this.bookingId = bookingId;
    }

    public int getUserId(){
        return userId;
    }

    public int getBookingId(){
        return bookingId;
    }


}
